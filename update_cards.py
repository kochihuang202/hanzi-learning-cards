def parse_filename(filename):
    basename = os.path.splitext(filename)[0]
    parts = basename.split('_')
    
    # 尋找長度為 1 且為中文的文字
    if len(parts) >= 2:
        for part in parts:
            if len(part) == 1 and '\u4e00' <= part <= '\u9fff':
                return part
        # 後備方案：如果第二個部分只有一個字
        if len(parts[1]) == 1:
            return parts[1]
        # 如果第一個部分只有一個字
        if len(parts[0]) == 1:
            return parts[0]
            
    # 如果都沒找到，搜尋檔名中的第一個中文字
    for char in basename:
        if '\u4e00' <= char <= '\u9fff':
            return char
    return basename

def get_sort_key(filepath):
    filename = os.path.basename(filepath)
    # 提取檔名前端的數字編號
    match = re.match(r'^(\d+)', filename)
    if match:
        return (0, int(match.group(1)), filename)
    else:
        return (1, 0, filename)

def load_existing_vocab(filepath):
    vocab_map = {}
    if not os.path.exists(filepath):
        return vocab_map
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
        # 尋找中括號內部的 JSON 陣列
        match = re.search(r'=\s*(\[[\s\S]*\]);?', content)
        if match:
            json_str = match.group(1)
            # 清理尾部逗號以防止 json.loads 解析失敗
            json_str = re.sub(r',\s*\]', ']', json_str)
            json_str = re.sub(r',\s*\}', '}', json_str)
            data = json.loads(json_str)
            for item in data:
                char = item.get('char')
                vocab = item.get('vocab')
                if char and vocab:
                    vocab_map[char] = vocab
    except Exception as e:
        print(f"提示: 無法讀取現有的 cards_data.js ({e})。將重新生成。")
    return vocab_map

def main():
    base_dir = os.path.dirname(os.path.abspath(__file__))
    cards_dir = os.path.join(base_dir, "國字認字卡")
    js_output_path = os.path.join(base_dir, "cards_data.js")
    
    # 載入現有已編輯的詞彙
    existing_vocab = load_existing_vocab(js_output_path)
    
    # 掃描所有圖片
    image_extensions = ('.png', '.jpg', '.jpeg', '.webp')
    image_paths = []
    
    if os.path.exists(cards_dir):
        for root, _, files in os.walk(cards_dir):
            for file in files:
                if file.lower().endswith(image_extensions):
                    full_path = os.path.join(root, file)
                    image_paths.append(full_path)
                    
    # 排序圖片（依據編號）
    image_paths.sort(key=get_sort_key)
    
    cards_data = []
    seen_chars = set()
    
    for idx, path in enumerate(image_paths, 1):
        filename = os.path.basename(path)
        # 取得相對路徑（用於網頁載入）
        rel_path = os.path.relpath(path, base_dir).replace('\\', '/')
        char = parse_filename(filename)
        
        # 避免重複國字
        if char in seen_chars:
            continue
        seen_chars.add(char)
        
        # 詞彙決定順序：
        # 1. 優先使用現有 cards_data.js 中已修改的詞彙
        # 2. 其次使用預定義的 VOCAB_DICT 詞彙
        # 3. 最後使用預留空位
        vocab = existing_vocab.get(char)
        if not vocab:
            vocab = VOCAB_DICT.get(char)
        if not vocab:
            vocab = [f"{char}字一", f"{char}字二", f"{char}字三", f"{char}字四", f"{char}字五"]
            
        cards_data.append({
            "id": idx,
            "char": char,
            "fileName": filename,
            "filePath": rel_path,
            "vocab": vocab
        })
        
    # 寫入 cards_data.js
    with open(js_output_path, 'w', encoding='utf-8') as f:
        f.write("// 本檔案為自動生成，網頁將會讀取此檔案的 CARDS_DATA 變數\n")
        f.write("// 如果新增圖片，請重新執行 python update_cards.py\n")
        f.write("const CARDS_DATA = ")
        json.dump(cards_data, f, ensure_ascii=False, indent=2)
        f.write(";\n")
        
    print(f"成功！已掃描 {len(cards_data)} 張卡片，並寫入 {js_output_path}")

if __name__ == "__main__":
    main()
